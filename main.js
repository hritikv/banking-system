let number = Math.floor((Math.random() * 100) + 1);
let chances = 0;
document.write("This is a guessing game and you have to enter a number and if it matches with the number generated by computer, then you win...\n");
let guess;
guess= prompt("Enter a number between 1 and 100");
chances++;
do{
  guess= Number.parseInt(guess);
  if (guess > number) {
    document.write("Number entered is greater");
    guess = prompt("Enter again!!");
    guess = Number.parseInt(guess);
    ++chances; 
    continue;
  }

  else if (guess < number) {
    document.write("Number entered is smaller");
    guess = prompt("Enter again!!");
    guess = Number.parseInt(guess);
    ++chances; 
  }

} while (guess != number); 

